# Numerical datatype

a = 12
print(a)

a = 45          # re-assigning value to a   # int
print(a)

b = 12.423      # floating point    # float
print(b)

# type()

print(type(a))
print(type(b))

# datatype  is type of data that we are going to do some action with it

# Boolean datatype

c = True
d = False     # bool

print(c)
print(d)

print(type(c))
print(type(d))


# String datatype  # string is a group character

name = 'Bascom'
name2 = "Bascom"   # str

print(name)
print(name2)

print(type(name))
print(type(name2))

